#Crie uma lista vazia chamada compras. Adicione 3 itens nela usando o append().

compras = [] #compras é a variáve, já o = chamamos de recebe e o [] é a lista vazia

compras.append("Camiseta") #Adicionar camiseta à lista compras
compras.append("Casaco")  #Adicionar Casaco à lista compras
compras.append("Calça")   #Adicionar Calça à lista compras
compras.remove("Casaco")  #Remove o item Casaco da lista de compras

print(compras) #Exibe compras
print (len(compras)) # Exibe tamanho de compras
